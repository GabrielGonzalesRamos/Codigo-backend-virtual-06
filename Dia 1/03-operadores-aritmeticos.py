numero1 = 10
numero2 = 20
# Si las  dos variables son enteras, todos los resultados a excepción de la division serán enteros

# + SUMA
numero1 + numero2
print(f"La suma es: {numero1 + numero2}")
# - RESTA
numero2 + numero1
print(f"La resta es: {numero2 - numero1}")
# * MULTIPLICACION
numero1 * numero2
print(f"La multiplicación es: {numero1 * numero2}")
# / DIVISON
numero1 / numero2
print(f"La division es: {numero1 / numero2}")
# % MODULO
print(f"El modulo: {numero1 % numero2}") 
# // COCIENTE
print(f"El cociente: {numero1 // numero2}") 
